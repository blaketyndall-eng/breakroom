/**
 * Nano Prompts — VOID illustration prompt library (PR-G / PR-H / PR-I / PR-J)
 *
 * Single source of truth for VOID-side image generation. Each NanoPrompt
 * declares its provider so the dispatcher can route paid hero slots to
 * Replicate FLUX 1.1 Pro and free fill slots to Pollinations.
 *
 * Routing rules:
 *   - 'pollinations-flux'    → free, FLUX schnell, ~700-char URL ceiling
 *   - 'pollinations-realism' → free, photoreal-ish
 *   - 'pollinations-turbo'   → free, fastest, lowest quality
 *   - 'pollinations-gptimage' → free, OpenAI-tier composition
 *   - 'replicate-flux-pro'   → paid (~$0.04/img), serves from /public/void/<key>.jpg
 *   - 'replicate-flux-dev'   → paid stub
 *   - 'replicate-sdxl'       → paid stub
 *   - 'replicate-imagen'     → paid stub
 *
 * Replicate-tier slots ARE NOT generated at request time. They are
 * pre-generated by `pnpm regen-images` and committed as binaries to
 * /public/void/<key>.jpg. The dispatcher simply returns the static path.
 */

export type Provider =
  // Free text-to-image (Pollinations.ai)
  | 'pollinations-flux'
  | 'pollinations-realism'
  | 'pollinations-turbo'
  | 'pollinations-gptimage'
  // Paid text-to-image (Replicate)
  | 'replicate-flux-pro'
  | 'replicate-flux-dev'
  | 'replicate-recraft-v3' // flat-illustration native prior — NO style ref via Replicate
  | 'replicate-sdxl'
  | 'replicate-imagen'
  // Paid image-to-image (Replicate) — requires anchorKey
  | 'replicate-flux-kontext-pro'
  // Locked binary committed to /public/void/<key>.jpg — no generation
  | 'static-asset';

